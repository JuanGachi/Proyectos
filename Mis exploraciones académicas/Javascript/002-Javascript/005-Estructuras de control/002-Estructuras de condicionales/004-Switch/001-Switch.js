var diadelasemana = "lunes"
switch(diadelasemana){
case "lunes":
    console.log("Hoy es el peor dia de la semana");
    break;
case "martes":
    console.log("Hoy es el segundo peor dia de la semana");
break;
case "miercoles":
    console.log("Ya estamos a mitad de semana");
break;
case "jueves":
    console.log("Mañana ya es viernes");
break;
case "viernes":
    console.log("Por fin es viernes");
break;
case "sabado":
    console.log("El mejor dia de la semana");
break;
case "domingo":
    console.log("Parece mentira que mañana ya sea lunes");
break;
default:
    console.log("No se que has escrito, pero no es un dia");
}