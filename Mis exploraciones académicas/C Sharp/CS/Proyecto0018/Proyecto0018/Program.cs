string diadelasemana = "Viernes";

switch (diadelasemana) {
    case "lunes":
        Console.WriteLine("hoy es el peor dia de la semana");
        break;

    case "martes":

        Console.WriteLine("hoy es el segundo peor dia de la semana");
        break;

    case "Miercoles":

        Console.WriteLine("Ya estamos a mitad de semana");
        break;

    case "Jueves":

        Console.WriteLine("Hoy es juernes");
        break;

    case "Viernes":

    Console.WriteLine("Ya viene el fin de semana");
        break;

    case "Sabado":

    Console.WriteLine("Hoy es el mejor dia de la semana");
        break;

    case "Domingo":

    Console.WriteLine("Parece mentira que hoy es domingo ya");
        break;

    default:

        Console.WriteLine("Lo que has escrito no es un dia");
        break ; 
}







